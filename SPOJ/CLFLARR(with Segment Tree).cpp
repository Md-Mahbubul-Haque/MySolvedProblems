class SGTree{
    vi st;
    vi ara;
    int n;
public:
    SGTree(int _n){
        n=_n;
        st.assign(4*n+5,0);
    }
    int left(int node){return (node<<1);}
    int right(int node){return (node<<1)|1;}

    void push(int node){
        if(st[node]!=0){
            st[left(node)]=st[right(node)]=st[node];
            st[node]=0;
        }
    }

    void update(int node,int L,int R,int i,int j,int c){
        if(i>R or j<L) return;
        if(R<=j and L>=i){
            st[node]=c;
            return;
        }
        int mid=(L+R)>>1;

        push(node);

        update(left(node),L,mid,i,j,c);
        update(right(node),mid+1,R,i,j,c);
    }

    int query(int node,int L,int R,int i){
        if(i<L or i>R) return -1;
        if(L==i and R==i) return st[node];

        push(node);

        int mid=(L+R)>>1;

        int val1=query(left(node),L,mid,i);
        int val2=query(right(node),mid+1,R,i);

        if(val1==-1) return val2;
        if(val2==-1) return val1;
    }
};

