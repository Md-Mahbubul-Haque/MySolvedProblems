ll dp[2002][2002];
int n,k;

ll ret(int id,int x){

    if(id>=k){
        return 1;
    }

    if(dp[id][x]!=-1) return dp[id][x];

    ll times=0;
    int now=x;

    while(now<=n){
        times=(times+ret(id+1,now))%MOD;
        now+=x;
    }

   /// cout<<id<<" "<<x<<" "<<times<<endl;
    return dp[id][x]=times;

}

